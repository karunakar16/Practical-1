fun main(){
    val i: Int=22
    val s: String="10"
    val d: Double=i.toDouble()
    val b: Int=s.toInt()
    val k: Int=s.toInt()
    val r: Double=s.toDouble()
    println("Integer value: " +i)
    println("Double value (from integer): "+d)
    println("String value: "+s)
    println("Integer value1 (from string): "+b)
    println("Integer value2 (from string): "+k)
    println("Double value (from string): "+r)
}